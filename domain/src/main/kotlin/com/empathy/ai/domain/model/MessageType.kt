package com.empathy.ai.domain.model

/**
 * AI军师对话消息类型枚举
 *
 * 用于区分对话中的用户消息和AI回复消息。
 * 这个枚举是UI布局的关键依据，决定消息气泡的位置和样式。
 *
 * 业务背景 (PRD-00026/3.1.1):
 * - AI消息显示在左侧，用户消息显示在右侧（类似微信/QQ布局）
 * - 支持长文本对话，自动换行显示
 * - 提供打字动画效果增强用户体验
 *
 * 设计决策 (TDD-00026/6.3.2):
 * - 简单的二元枚举，避免过度设计
 * - UI层根据类型决定alignment和backgroundColor
 * - 类型判断用于显示不同的角色标签（"AI军师" vs "我"）
 *
 * 任务追踪 (FD-00026/T003): 数据层实现 - 消息类型枚举
 */
enum class MessageType {
    /** 用户发送的消息 - 显示在右侧，使用主色调背景 */
    USER,

    /** AI军师的回复消息 - 显示在左侧，使用次级色调背景 */
    AI
}
