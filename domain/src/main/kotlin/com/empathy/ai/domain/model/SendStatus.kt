package com.empathy.ai.domain.model

/**
 * 消息发送状态枚举
 *
 * 用于跟踪消息的发送状态，支持UI显示发送进度和重试功能。
 * 这个枚举是错误恢复机制的核心，支持用户主动重试失败的消息。
 *
 * 业务背景 (PRD-00026/验收标准):
 * - 支持消息发送失败后的重试功能
 * - 用户可删除发送失败的消息
 * - 错误处理友好，有明确提示
 *
 * 设计决策 (TDD-00026/13.4):
 * - PENDING: 用于AI消息，表示正在生成中（用户消息也用PENDING表示等待AI响应）
 * - SUCCESS: 消息发送成功或AI响应已完成
 * - FAILED: 消息发送失败或AI调用错误，显示重试按钮
 *
 * 任务追踪 (FD-00026/T003): 数据层实现 - 发送状态枚举
 */
enum class SendStatus {
    /**
     * 发送中/等待中
     * - 用户消息: 等待AI响应
     * - AI消息: 流式生成中
     */
    PENDING,

    /**
     * 发送成功
     * - 消息已成功发送或AI响应已完成
     * - 正常状态，不显示任何错误UI
     */
    SUCCESS,

    /**
     * 发送失败
     * - 网络错误/API错误/超时等
     * - UI显示重试按钮和错误提示
     */
    FAILED
}
