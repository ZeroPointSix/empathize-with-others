package com.empathy.ai.presentation.ui.screen.contact

/**
 * 详情页标签页枚举
 *
 * 定义联系人详情页的四个主要界面
 */
enum class DetailTab {
    /**
     * 概览
     * 展示关系概况、核心标签、最新动态
     */
    Overview,
    
    /**
     * 事实流
     * 展示时间线和对话历史
     */
    FactStream,
    
    /**
     * 标签画像
     * 展示和管理联系人标签
     */
    Persona,
    
    /**
     * 资料库
     * 展示数据源和处理状态
     */
    DataVault
}
